//Using String.endsWith()
//Similarly we have String.startsWith();
class StringEndsWith{
    public static void main(String args[]){
        String s1="keerthi";
        if(s1.endsWith("i")){
            System.out.println("Yes the string ends with i");
        }
        else{
            System.out.println("no the string does not ends with i");
        }
    }
}